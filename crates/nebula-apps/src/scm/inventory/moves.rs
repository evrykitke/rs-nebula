/// The four kinds of stock movement document.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Serialize, Deserialize, utoipa::ToSchema)]
#[serde(rename_all = "snake_case")]
pub enum MoveType {
    Receipt,
    Issue,
    Transfer,
    Adjustment,
}

impl MoveType {
    pub fn as_str(self) -> &'static str {
        match self {
            MoveType::Receipt => "receipt",
            MoveType::Issue => "issue",
            MoveType::Transfer => "transfer",
            MoveType::Adjustment => "adjustment",
        }
    }

    pub fn parse(value: &str) -> Result<Self> {
        match value {
            "receipt" => Ok(MoveType::Receipt),
            "issue" => Ok(MoveType::Issue),
            "transfer" => Ok(MoveType::Transfer),
            "adjustment" => Ok(MoveType::Adjustment),
            other => Err(Error::internal(format!("unknown move type {other:?}"))),
        }
    }

    /// The number series this document type draws from at post.
    pub fn series(self) -> &'static str {
        match self {
            MoveType::Receipt => crate::scm::RECEIPT_SERIES,
            MoveType::Issue => crate::scm::ISSUE_SERIES,
            MoveType::Transfer => crate::scm::TRANSFER_SERIES,
            MoveType::Adjustment => crate::scm::ADJUSTMENT_SERIES,
        }
    }
}

/// Where a movement document is in its lifecycle.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Serialize, Deserialize, utoipa::ToSchema)]
#[serde(rename_all = "snake_case")]
pub enum MoveStatus {
    Draft,
    Posted,
    Reversed,
}

impl MoveStatus {
    pub fn as_str(self) -> &'static str {
        match self {
            MoveStatus::Draft => "draft",
            MoveStatus::Posted => "posted",
            MoveStatus::Reversed => "reversed",
        }
    }

    pub fn parse(value: &str) -> Result<Self> {
        match value {
            "draft" => Ok(MoveStatus::Draft),
            "posted" => Ok(MoveStatus::Posted),
            "reversed" => Ok(MoveStatus::Reversed),
            other => Err(Error::internal(format!("unknown move status {other:?}"))),
        }
    }
}

