/// Adjustments have their own permission branch; everything else shares
/// the movements branch.
fn perm_view(t: MoveType) -> &'static str {
    match t {
        MoveType::Adjustment => names::ADJUSTMENTS_VIEW,
        _ => names::MOVEMENTS_VIEW,
    }
}

fn perm_create(t: MoveType) -> &'static str {
    match t {
        MoveType::Adjustment => names::ADJUSTMENTS_CREATE,
        _ => names::MOVEMENTS_CREATE,
    }
}

fn perm_post(t: MoveType) -> &'static str {
    match t {
        MoveType::Adjustment => names::ADJUSTMENTS_POST,
        _ => names::MOVEMENTS_POST,
    }
}

