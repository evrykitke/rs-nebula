//! Stock movement documents: the user-facing papers that drive the engine.
//!
//! Four document types share one lifecycle — draft (editable, unnumbered)
//! → posted (numbered gap-free, immutable, stock applied) → reversed
//! (mirrored by a linked reversal document):
//!
//! - **receipt** — stock in at a given cost (direct/opening receipt)
//! - **issue** — stock out at the running average
//! - **transfer** — out of one warehouse, into another at the issued cost
//!   (a transfer never creates or destroys value)
//! - **adjustment** — lines carry the *counted* quantity; the delta against
//!   on-hand is computed under the level lock at post time; counting up
//!   from zero needs a unit cost (this doubles as opening stock)
//!
//! Posting locks in a fixed order everywhere: the document row first, then
//! every touched level row ascending by `(item_id, warehouse_id)`, then
//! the numbering series row — so concurrent posts serialize instead of
//! deadlocking. A movement generated by another document (`source` set,
//! e.g. a procurement goods receipt) can only be reversed through that
//! source document.

